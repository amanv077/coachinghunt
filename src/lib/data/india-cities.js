export function searchCities(query, limit = 10) {
  const q = query?.trim().toLowerCase();
  if (!q) return [];

  const matches = INDIA_CITIES.filter((city) => city.toLowerCase().includes(q));
  const unique = [...new Set(matches)];
  return unique.slice(0, limit).map((name) => ({ name }));
}
