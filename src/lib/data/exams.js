export function searchExams(query, limit = 10) {
  const q = query?.trim().toLowerCase();
  if (!q) return [];

  const matches = EXAMS.filter((exam) => exam.toLowerCase().includes(q));
  return matches.slice(0, limit).map((name) => ({ name }));
}
